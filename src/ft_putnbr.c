#include "libft.h"

void	ft_putnbr(int n)
{
	if (!(n / 10))
	{
		if (n < 0)
			ft_putchar('-');
	}
	else
		ft_putnbr(n / 10);
	n = -(n < 0) * (n % 10);
	ft_putchar(n + '0');
}
