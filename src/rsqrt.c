#include "libft.h"

float	rsqrt(float n)
{
	t_conv	conv;

	conv.f = n;
	conv.i = 0x5f1ffff9 - (conv.i >> 1);
	conv.f *= 0.703952253f * (2.38924456f - n * conv.f * conv.f);
	return (conv.f);
}
